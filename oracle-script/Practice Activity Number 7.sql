SELECT DATE '2022-05-04' AS Result
FROM DUAL;

SELECT TIMESTAMP '2022-05-04 13:30:01' AS Result
FROM DUAL;

SELECT TIMESTAMP '2022-05-04 13:30:01' +7 as Result
FROM DUAL;

SELECT ADD_MONTHS(TIMESTAMP '2022-05-04 13:30:01', 1) as monthEarlier,
ADD_MONTHS(TIMESTAMP '2022-05-04 13:30:01', -1) as monthLater
FROM DUAL;


SELECT EXTRACT(MONTH FROM TIMESTAMP '2022-05-04 13:30:01' ) as Result
FROM DUAL;

SELECT TRUNC(TO_DATE('2022-05-04 13:30:01', 'YYYY-MM-DD HH24:MI:SS') ) as Result
FROM DUAL;

SELECT 'The date is ' || TRIM(TO_CHAR(CURRENT_DATE,'YYYY-MM-DD HH24:MI:SS' || '.')) as Result
FROM DUAL;


